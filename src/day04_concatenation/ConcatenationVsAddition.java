package day04_concatenation;

public class ConcatenationVsAddition {
    public static void main(String[] args) {
        System.out.println("32" + 1); //32 in double quote is string. Prints as 321
        System.out.println(7 + "1"); //71
        System.out.println("13" + "2"); //132
        System.out.println("_____________________________________");

        System.out.println(12 + 2.5); //Addition, no double quote
        System.out.println("_____________________________");

        System.out.println("the value is " + true);










    }






}
