package day04_concatenation;

public class ConcatenationIntro {
    public static void main(String[] args) {

        String name =  "Gulustan";
        int age = 35;
        System.out.println("Hello " + name + "." + age + " years old."); //concatenation

        System.out.println("You are " + age + " years old.");

        int salary = 85_000; //_makes it readable and it does not print as 85_000, prints 85000
        System.out.println("You salary is " + salary + " USD");

        int number = 100_000_000;
        System.out.println("number = " + number);








    }





}
