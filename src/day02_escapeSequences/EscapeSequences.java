package day02_escapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Hello students\nToday we have java class."); //  \n for new line

        System.out.println("------------------------");
        System.out.println("\tJava Programming Language"); // \t for new paragraph (tab)
        System.out.println("\t\tJava Developer Course Batch"); // \t\t is for double tab
        System.out.println("-------------------------");
        System.out.println("\\");  // Double backslash in "\\" for printing one backslash
        System.out.println("--------------------------");
        System.out.println("My favorite TV show is \"The Walking Dead\"");
        //To use Double quote in double quotestart and end with \" Exp: ("xyz \"abc\"");




    }



}
